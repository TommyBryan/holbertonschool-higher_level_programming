"""
Tuple - ()
 Ordered: Elements keep their order you add them
 Inmutable: You cannot change them, you cannot add or remove elements
 Allows duplicates: You can add the same element multiple times
 Uses: When you want to keep the order of elements, and you don't want to allow duplicates
"""
# Example:

my_tuple = (1, 2, 3, 2)
print(my_tuple)  # Output: (1, 2, 3, 2)
